    private void acceder() {
        ControladorLogin login = new ControladorLogin(txtUsuario.getText(), txtPass.getPassword());
        int resultado = login.validar();
        switch (resultado) {
            case 0:
                if (login.acceder()) {
                    dispose();
                }
                break;
            case 1:
                txtUsuario.requestFocus();
                break;
            case 2:
                txtPass.requestFocus();
                break;
        }

    }

